#!/usr/bin/env python3

# Python Primitive Data Types

# String

# Print a specific character from a string (subscripting).
print("Hello"[0])

# Concatenate two strings together.
print("123" + "345")

print("Hello " + "World")


# Integer

# Without the quotes these numbers are integers.
print(123 + 345)

# To seperate thousands from hundreds. This is equal to 123456789 in Python.
a = 123_456_789
print(a + 1)


# Float (Floating Point Number)

# For example pi is a float as it has a decimal place.
3.14159


# Boolean

True
False